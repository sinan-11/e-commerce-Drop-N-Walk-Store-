import axios from "axios"
import React, { useEffect, useState } from "react"
import { useNavigate, useParams } from "react-router-dom"
import { toast } from "react-toastify"

function EditProducts() {
  const { id } = useParams()
  const navigate = useNavigate()

  const [loading, setLoading] = useState(true)
  const [saving, setSaving] = useState(false)

  const [form, setForm] = useState({
    name: "",
    brand: "",
    category: "",
    gender: "",
    price: "",
    description: "",
    images: [],
    sizes: []
  })

  /* ================= FETCH PRODUCT ================= */
  useEffect(() => {
    axios
      .get("http://localhost:5000/products")
      .then(res => {
        const product = res.data.find(p => String(p.id) === String(id))
        if (!product) {
          toast.error("Product not found")
          navigate("/admin/products")
          return
        }
        setForm({
          name: product.name || "",
          brand: product.brand || "",
          category: product.category || "",
          gender: product.gender || "",
          price: product.price || "",
          description: product.description || "",
          images: product.images || [],
          sizes: product.sizes || []
        })
      })
      .catch(() => toast.error("Failed to load product"))
      .finally(() => setLoading(false))
  }, [id, navigate])

  /* ================= HANDLERS ================= */
  const handleChange = e =>
    setForm({ ...form, [e.target.name]: e.target.value })

  const handleImageChange = (index, value) => {
    const updated = [...form.images]
    updated[index] = value
    setForm({ ...form, images: updated })
  }

  const addImage = () =>
    setForm({ ...form, images: [...form.images, ""] })

  const removeImage = index =>
    setForm({
      ...form,
      images: form.images.filter((_, i) => i !== index)
    })

  const toggleSize = size =>
    setForm(prev => ({
      ...prev,
      sizes: prev.sizes.includes(size)
        ? prev.sizes.filter(s => s !== size)
        : [...prev.sizes, size]
    }))

  /* ================= SUBMIT ================= */
  const handleSubmit = e => {
    e.preventDefault()
    setSaving(true)

    axios
      .put(`http://localhost:5000/products/${id}`, {
        ...form,
        price: Number(form.price)
      })
      .then(() => {
        toast.success("Product updated successfully")
        navigate("/admin/products")
      })
      .catch(() => toast.error("Update failed"))
      .finally(() => setSaving(false))
  }

  if (loading) {
    return (
      <div className="min-h-screen bg-zinc-50 flex items-center justify-center">
        <div className="flex flex-col items-center gap-3">
          <div className="w-7 h-7 border-2 border-zinc-200 border-t-zinc-900 rounded-full animate-spin" />
          <p className="text-xs tracking-widest uppercase text-zinc-400">
            Loading product
          </p>
        </div>
      </div>
    )
  }

  return (
    <div className="min-h-screen bg-zinc-50 px-6 py-16">
      <div className="max-w-4xl mx-auto">

        {/* HEADER */}
        <div className="mb-10">
          <p className="text-xs uppercase tracking-[0.25em] text-zinc-400 font-semibold">
            Inventory
          </p>
          <h1 className="text-3xl font-semibold tracking-tight text-zinc-900 mt-1">
            Edit Product
          </h1>
        </div>

        {/* FORM CARD */}
        <div className="bg-white border border-zinc-200 rounded-2xl shadow-sm p-8">
          <form onSubmit={handleSubmit} className="space-y-10">

            {/* BASIC INFO */}
            <section className="space-y-6">
              <Field label="Product Name">
                <input
                  name="name"
                  value={form.name}
                  onChange={handleChange}
                  className="input"
                  required
                />
              </Field>

              <div className="grid grid-cols-1 md:grid-cols-3 gap-5">
                <Field label="Brand">
                  <input
                    name="brand"
                    value={form.brand}
                    onChange={handleChange}
                    className="input"
                  />
                </Field>

                <Field label="Category">
                  <input
                    name="category"
                    value={form.category}
                    onChange={handleChange}
                    className="input"
                  />
                </Field>

                <Field label="Gender">
                  <select
                    name="gender"
                    value={form.gender}
                    onChange={handleChange}
                    className="input bg-white"
                  >
                    <option value="">Select</option>
                    <option>Men</option>
                    <option>Women</option>
                    <option>Unisex</option>
                  </select>
                </Field>
              </div>

              <Field label="Price">
                <input
                  type="number"
                  name="price"
                  value={form.price}
                  onChange={handleChange}
                  className="input"
                />
              </Field>

              <Field label="Description">
                <textarea
                  rows="4"
                  name="description"
                  value={form.description}
                  onChange={handleChange}
                  className="input resize-none"
                />
              </Field>
            </section>

            {/* IMAGES */}
            <section>
              <h3 className="section-title">Product Images</h3>

              <div className="space-y-3">
                {form.images.map((img, i) => (
                  <div key={i} className="flex gap-3">
                    <input
                      value={img}
                      onChange={e => handleImageChange(i, e.target.value)}
                      placeholder={`Image URL ${i + 1}`}
                      className="input flex-1"
                    />
                    <button
                      type="button"
                      onClick={() => removeImage(i)}
                      className="px-3 rounded-lg bg-red-50 text-red-600 hover:bg-red-100"
                    >
                      âœ•
                    </button>
                  </div>
                ))}
              </div>

              <button
                type="button"
                onClick={addImage}
                className="mt-4 px-4 py-2 text-sm rounded-lg bg-zinc-900 text-white hover:bg-zinc-700"
              >
                + Add Image
              </button>

              {form.images.length > 0 && (
                <div className="flex gap-3 flex-wrap mt-4">
                  {form.images.map(
                    (img, i) =>
                      img && (
                        <img
                          key={i}
                          src={img}
                          alt=""
                          className="w-24 h-24 object-cover rounded-xl border"
                        />
                      )
                  )}
                </div>
              )}
            </section>

            {/* SIZES */}
            <section>
              <h3 className="section-title">Available Sizes</h3>
              <div className="flex gap-2 flex-wrap">
                {[6, 7, 8, 9, 10, 11].map(size => (
                  <button
                    key={size}
                    type="button"
                    onClick={() => toggleSize(size)}
                    className={`px-4 py-2 rounded-lg border text-sm font-medium transition
                      ${
                        form.sizes.includes(size)
                          ? "bg-zinc-900 text-white border-zinc-900"
                          : "bg-white hover:border-zinc-400"
                      }`}
                  >
                    {size}
                  </button>
                ))}
              </div>
            </section>

            {/* ACTIONS */}
            <div className="flex flex-col items-end gap-3 pt-6 border-t">
              <button
                type="button"
                onClick={() => navigate("/admin/products")}
                className="w-40 px-6 py-2 rounded-lg border text-sm hover:bg-zinc-50"
              >
                Cancel
              </button>

              <button
                type="submit"
                disabled={saving}
                className="w-40 px-6 py-2 rounded-lg bg-zinc-900 text-white text-sm hover:bg-zinc-700"
              >
                {saving ? "Saving..." : "Update Product"}
              </button>
            </div>

          </form>
        </div>
      </div>

      {/* SHARED STYLES */}
      <style>{`
        .input {
          width: 100%;
          padding: 0.75rem;
          border: 1px solid #e4e4e7;
          border-radius: 0.75rem;
          font-size: 0.875rem;
          outline: none;
        }
        .input:focus {
          border-color: #18181b;
          box-shadow: 0 0 0 2px rgba(24,24,27,0.08);
        }
        .section-title {
          font-size: 0.875rem;
          font-weight: 600;
          color: #18181b;
          margin-bottom: 0.75rem;
        }
      `}</style>
    </div>
  )
}

/* ===== FIELD WRAPPER ===== */
const Field = ({ label, children }) => (
  <div>
    <label className="block text-xs font-medium text-zinc-500 mb-1">
      {label}
    </label>
    {children}
  </div>
)

export default EditProducts